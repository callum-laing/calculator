//function for addition
let addNumbers = (num1, num2) => {
  return num1 + num2;
};

//function for subtraction
let subNumbers = (num1, num2) => {
  return num1 - num2;
};

//function for multiplicaion
let multiplyNumbers = (num1, num2) => {
  return num1 * num2;
};

//function for division
let divideNumbers = (num1, num2) => {
  return num1 / num2;
};
